/*
笔记:
  1. unknown 类似于 any，表示任意值，但更安全，因为对 unknown 执行任何操作都是非法的
  2. 类型中可以直接使用展开操作符展开数组，而无需通过 infer 取出值
*/
type ObjectEntries<T, Copy = keyof T> = Copy extends keyof T
  ? T[Copy] extends undefined
    ? [Copy, T[Copy]]
    : [Copy, Required<T>[Copy]]
  : never

